//go:build !windows

package auth

import (
	"fmt"
	"os/exec"
	"runtime"
)

func openBrowser(url string) error {
	if err := validateBrowserURL(url); err != nil {
		return err
	}

	var cmd *exec.Cmd
	switch runtime.GOOS {
	case "darwin":
		cmd = exec.Command("open", url)
	case "linux":
		cmd = exec.Command("xdg-open", url)
	default:
		return fmt.Errorf("unsupported platform: %s", runtime.GOOS)
	}
	return cmd.Start()
}
